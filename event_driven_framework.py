# 4. RISK MANAGEMENT ENGINE
# ============================================================================

class RiskManagementEngine:
    """
    Receives ScoredTradeIntent and enforces risk parameters:
    - Validates AI Score > 60.0.
    - Calculates position size using Fixed Fractional Risk Model:
        Position_Size = (Account_Balance * 0.01) / ATR
    - Sets dynamic Stop-Loss at 1.5 * ATR below entry price.
    - Sets Take-Profit at 3.0 * ATR above entry price.
    """

    def __init__(self, min_ai_threshold: float = 60.0, risk_fraction: float = 0.01):
        self.min_ai_threshold = min_ai_threshold
        self.risk_fraction = risk_fraction  # 1% per trade

    def process_scored_intent(
        self,
        scored_intent: ScoredTradeIntent,
        account_balance: float = 10000.0
    ) -> Optional[ApprovedOrder]:
        """
        Validates AI score threshold and calculates position sizing & SL/TP levels.
        """
        intent = scored_intent.trade_intent
        ai_score = scored_intent.ai_score

        if ai_score <= self.min_ai_threshold:
            print(f"  [RISK REJECTED] AI Score {ai_score} <= threshold {self.min_ai_threshold}. Trade aborted.")
            return None

        # Fixed Fractional Sizing calculation
        risk_amount_usdt = account_balance * self.risk_fraction
        atr = max(intent.atr, 0.1)
        position_size = round(risk_amount_usdt / atr, 4)

        # SL and TP calculation
        stop_loss = round(intent.entry_price - (1.5 * atr), 4)
        take_profit = round(intent.entry_price + (3.0 * atr), 4)

        return ApprovedOrder(
            symbol=intent.symbol,
            side=intent.side,
            entry_price=intent.entry_price,
            position_size=position_size,
            stop_loss=stop_loss,
            take_profit=take_profit,
            ai_score=ai_score,
            account_balance=account_balance,
            risk_amount_usdt=risk_amount_usdt,
            status="APPROVED_AND_READY",
            timestamp=intent.timestamp
        )


