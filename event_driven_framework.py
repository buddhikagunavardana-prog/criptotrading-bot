# DATA STRUCTURES
# ============================================================================

@dataclass
class CandleTick:
    """Represents an incoming market tick/candle."""
    symbol: str
    timestamp: str
    open: float
    high: float
    low: float
    close: float
    volume: float


@dataclass
class MarketFeatures:
    """Processed market features generated by FeatureEngine."""
    symbol: str
    timestamp: str
    current_price: float
    vwap: float
    atr: float
    regime_state: int      # 0 = Ranging, 1 = Trending
    regime_label: str      # 'Ranging' or 'Trending'
    price_vwap_diff: float


@dataclass
class TradeIntent:
    """Signal output from StrategyEngine."""
    symbol: str
    timestamp: str
    side: str              # 'BUY' or 'SELL'
    entry_price: float
    vwap: float
    atr: float
    regime: str
    reason: str


@dataclass
class ScoredTradeIntent:
    """Scored signal output from AIScoringEngine."""
    trade_intent: TradeIntent
    ai_score: float        # 0.0 - 100.0
    model_version: str


@dataclass
class ApprovedOrder:
    """Final risk-approved execution order output from RiskManagementEngine."""
    symbol: str
    side: str
    entry_price: float
    position_size: float
    stop_loss: float
    take_profit: float
    ai_score: float
    account_balance: float
    risk_amount_usdt: float
    status: str
    timestamp: str


