# 1. FEATURE ENGINE
# ============================================================================

class FeatureEngine:
    """
    Ingests OHLCV candle data and computes:
    - VWAP (Volume-Weighted Average Price)
    - ATR (Average True Range)
    - Market Regime Detection via Hidden Markov Model (HMM)
    """

    def __init__(self, atr_period: int = 14, hmm_n_components: int = 2):
        self.atr_period = atr_period
        self.hmm_n_components = hmm_n_components
        self.history: List[CandleTick] = []
        self.hmm_model: Optional[Any] = None
        self.is_hmm_fitted = False

        if HAS_HMMLEARN:
            self.hmm_model = GaussianHMM(
                n_components=self.hmm_n_components,
                covariance_type="diag",
                n_iter=100,
                random_state=42
            )

    def update(self, candle: CandleTick) -> MarketFeatures:
        """Incorporate a new candle tick and recompute technical indicators & regime state."""
        self.history.append(candle)

        vwap = self._calculate_vwap()
        atr = self._calculate_atr()
        regime_state, regime_label = self._predict_regime()

        price_diff = candle.close - vwap

        return MarketFeatures(
            symbol=candle.symbol,
            timestamp=candle.timestamp,
            current_price=candle.close,
            vwap=round(vwap, 4),
            atr=round(atr, 4),
            regime_state=regime_state,
            regime_label=regime_label,
            price_vwap_diff=round(price_diff, 4)
        )

    def _calculate_vwap(self) -> float:
        """Calculates Volume-Weighted Average Price across available history."""
        if not self.history:
            return 0.0

        if HAS_PANDAS_NUMPY:
            closes = np.array([c.close for c in self.history])
            highs = np.array([c.high for c in self.history])
            lows = np.array([c.low for c in self.history])
            volumes = np.array([c.volume for c in self.history])

            typical_prices = (highs + lows + closes) / 3.0
            total_vol = np.sum(volumes)
            if total_vol == 0:
                return float(closes[-1])
            return float(np.sum(typical_prices * volumes) / total_vol)
        else:
            total_pv = 0.0
            total_vol = 0.0
            for c in self.history:
                tp = (c.high + c.low + c.close) / 3.0
                total_pv += tp * c.volume
                total_vol += c.volume
            return total_pv / total_vol if total_vol > 0 else self.history[-1].close

    def _calculate_atr(self) -> float:
        """Calculates Average True Range (ATR) over period."""
        if len(self.history) < 2:
            return max(self.history[-1].high - self.history[-1].low, 1.0) if self.history else 1.0

        tr_list = []
        for i in range(1, len(self.history)):
            curr = self.history[i]
            prev = self.history[i - 1]
            tr = max(
                curr.high - curr.low,
                abs(curr.high - prev.close),
                abs(curr.low - prev.close)
            )
            tr_list.append(tr)

        window = tr_list[-self.atr_period:]
        return sum(window) / len(window) if window else 1.0

    def _predict_regime(self) -> tuple[int, str]:
        """
        Fits/predicts current market regime using Gaussian HMM based on price returns and volatility.
        State 1 = Trending, State 0 = Ranging.
        """
        if len(self.history) < 10:
            return 1, "Trending"  # Default initial assumption

        returns = [
            (self.history[i].close - self.history[i-1].close) / self.history[i-1].close
            for i in range(1, len(self.history))
        ]

        if HAS_HMMLEARN and len(returns) >= 20:
            try:
                X = np.column_stack([returns])
                if not self.is_hmm_fitted:
                    self.hmm_model.fit(X)
                    self.is_hmm_fitted = True
                
                hidden_states = self.hmm_model.predict(X)
                latest_state = int(hidden_states[-1])
                label = "Trending" if latest_state == 1 else "Ranging"
                return latest_state, label
            except Exception:
                pass

        # Fallback Statistical Regime Classifier if hmmlearn is unavailable
        latest_return = returns[-1]
        recent_std = sum(abs(r) for r in returns[-10:]) / 10.0
        if abs(latest_return) > (0.7 * recent_std):
            return 1, "Trending"
        return 0, "Ranging"


