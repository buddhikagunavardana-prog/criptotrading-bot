# 2. STRATEGY ENGINE
# ============================================================================

class StrategyEngine:
    """
    Evaluates incoming MarketFeatures from FeatureEngine.
    Strategy Logic:
    - IF regime is 'Trending' (State 1)
    - AND price pulls back to and bounces off/crosses above VWAP
    - THEN generates a 'Trade Intent' signal (BUY).
    """

    def __init__(self, vwap_tolerance_pct: float = 0.003):
        self.vwap_tolerance_pct = vwap_tolerance_pct
        self.prev_features: Optional[MarketFeatures] = None

    def evaluate(self, features: MarketFeatures) -> Optional[TradeIntent]:
        """
        Evaluates market features for signal conditions.
        Returns TradeIntent object if conditions are satisfied, else None.
        """
        if self.prev_features is None:
            self.prev_features = features
            return None

        # Rule 1: Regime must be Trending
        is_trending = (features.regime_label == "Trending" or features.regime_state == 1)

        # Rule 2: VWAP Bounce/Cross detection
        # Price was near/below VWAP in previous candle and crossed above VWAP in current candle
        prev_p = self.prev_features.current_price
        prev_vwap = self.prev_features.vwap
        curr_p = features.current_price
        curr_vwap = features.vwap

        # Check if price bounced near VWAP (within tolerance) or crossed above VWAP
        distance_to_vwap = abs(curr_p - curr_vwap) / curr_vwap
        is_near_vwap = distance_to_vwap <= self.vwap_tolerance_pct
        is_vwap_crossover = (prev_p <= prev_vwap) and (curr_p > curr_vwap)
        is_vwap_bounce = is_near_vwap and (curr_p >= curr_vwap)

        self.prev_features = features

        if is_trending and (is_vwap_crossover or is_vwap_bounce):
            reason = "HMM Regime Trending + VWAP Cross/Bounce detected"
            return TradeIntent(
                symbol=features.symbol,
                timestamp=features.timestamp,
                side="BUY",
                entry_price=features.current_price,
                vwap=features.vwap,
                atr=features.atr,
                regime=features.regime_label,
                reason=reason
            )

        return None


